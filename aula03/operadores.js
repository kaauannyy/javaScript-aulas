// Operador de comp. 
let nu1 = 20;
let nu2 = 150;
// Operador de comp. igual ==
console.log(nu1 == nu2);

//Operador de comp. diferente !=
console.log(nu1 != nu2);

//Operador de comp. valores e tipos iguais. ===
console.log(nu1 === nu2);

console.log(nu1 > nu2);
//Operador de comp. maior.

console.log(nu1 >= nu2);
//Operador de comp. maior ou igual.

console.log(nu1 <= nu2);
//Operador de comp. menor.

console.log(nu1 < nu2);
//Operador de comp. mmenor ou igual. 